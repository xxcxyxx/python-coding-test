"""
문제명: 아이스 아메리카노
출처: Programmers
유형: Implementation / Arithmetic
난이도: Level 0

상태: Solved
날짜: 2026-07-22

문제 요약:
- 아이스 아메리카노 한 잔의 가격은 5,500원이다.
- 가진 돈으로 살 수 있는 최대 아메리카노 잔 수를 구한다.
- 아메리카노를 구매한 후 남는 돈을 구한다.
- 잔 수와 남은 돈을 리스트로 반환한다.

풀이 접근:
1. 아이스 아메리카노 한 잔의 가격을 5,500원으로 정한다.
2. money를 가격으로 나눈 몫을 구한다.
3. 몫은 최대로 살 수 있는 아메리카노 잔 수이다.
4. money를 가격으로 나눈 나머지를 구한다.
5. 나머지는 아메리카노를 사고 남은 돈이다.
6. 잔 수와 남은 돈을 리스트에 담아 반환한다.

핵심 개념:
- // 연산자는 나눗셈의 몫을 구한다.
- % 연산자는 나눗셈의 나머지를 구한다.

시간 복잡도:
- 단순한 사칙연산만 수행하므로 O(1)이다.
"""


def solution(money):
    # 아이스 아메리카노 한 잔의 가격
    price = 5500

    # 가진 돈으로 최대로 살 수 있는 아메리카노 잔 수
    cups = money // price

    # 아메리카노를 사고 남은 돈
    change = money % price

    # 잔 수와 남은 돈을 리스트에 담아 반환
    return [cups, change]
