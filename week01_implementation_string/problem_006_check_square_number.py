"""
문제명: 정수 제곱근 판별
출처: Programmers
유형: Implementation / Math
난이도: Level 1
상태: Solved
날짜: 2026-06-12

문제 요약:
- 정수 n이 어떤 양의 정수 x의 제곱인지 판별한다.
- n이 x의 제곱이라면 (x + 1)의 제곱을 반환한다.
- n이 x의 제곱이 아니라면 -1을 반환한다.

풀이 접근:
1. n ** 0.5를 이용해 n의 제곱근을 구한다.
2. int()를 사용해 제곱근의 정수 부분을 x에 저장한다.
3. x * x == n이면 n은 완전제곱수라고 판단한다.
4. 완전제곱수라면 (x + 1) ** 2를 반환한다.
5. 완전제곱수가 아니라면 -1을 반환한다.

시간복잡도:
- O(1)

배운 점:
- n ** 0.5를 이용하면 제곱근을 구할 수 있다.
- 완전제곱수 여부는 x * x == n으로 확인할 수 있다.
- 조건을 만족하지 않는 경우의 return 값도 반드시 처리해야 한다.
"""

def solution(n):
    x = int(n ** 0.5)
    
    if x * x == n:
        return (x + 1) ** 2
    
    return -1
