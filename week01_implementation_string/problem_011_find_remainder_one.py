"""
문제명: 나머지가 1이 되는 수 찾기
출처: Programmers
유형: Implementation / Math / Brute Force
난이도: Level 1
상태: Solved
날짜: 2026-06-12

문제 요약:
- 자연수 n을 x로 나누었을 때 나머지가 1이 되는 가장 작은 자연수 x를 반환한다.

풀이 접근:
1. 가장 작은 x를 찾아야 하므로 2부터 차례대로 확인한다.
2. n % x == 1 조건을 만족하는지 검사한다.
3. 처음 조건을 만족하는 x가 가장 작은 값이므로 바로 반환한다.

시간복잡도:
- O(N), N은 입력값 n

배운 점:
- 가장 작은 값을 찾는 문제는 작은 값부터 순차 탐색하면 된다.
- 조건을 처음 만족하는 순간 return 하면 불필요한 탐색을 줄일 수 있다.
"""

def solution(n):
    for x in range(2, n):
        if n % x == 1:
            return x
