"""
문제명: 자연수 뒤집어 배열로 만들기
출처: Programmers
유형: Implementation / String / Array
난이도: Level 1
상태: Solved
날짜: 2026-06-11

문제 요약:
- 자연수 n을 뒤집어 각 자리 숫자를 원소로 가지는 배열로 반환한다.

풀이 접근:
1. 자연수 n을 문자열로 변환한다.
2. 문자열 슬라이싱 [::-1]을 이용해 뒤집는다.
3. 뒤집힌 각 문자를 정수로 변환해 리스트에 담는다.

시간복잡도:
- O(K), K는 n의 자릿수

배운 점:
- 문자열 슬라이싱 [::-1]을 사용하면 순서를 쉽게 뒤집을 수 있다.
- 자릿수 문제는 문자열 변환 후 순회하면 구현이 간단해진다.
"""

def solution(n):
    return [int(num) for num in str(n)[::-1]]
