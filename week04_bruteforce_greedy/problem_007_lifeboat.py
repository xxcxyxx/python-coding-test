"""
문제명: 구명보트
출처: Programmers
유형: Greedy / Sort / Two Pointer
난이도: Level 2
상태: Solved
날짜: 2026-06-26

문제 요약:
- 사람들의 몸무게 people과 구명보트의 무게 제한 limit이 주어진다.
- 구명보트에는 최대 2명까지 탈 수 있다.
- 보트에 탄 사람들의 몸무게 합은 limit을 넘을 수 없다.
- 모든 사람을 구출하기 위해 필요한 구명보트 개수의 최솟값을 구해야 한다.

풀이 접근:
1. 사람들의 몸무게를 오름차순으로 정렬한다.
2. 가장 가벼운 사람을 left, 가장 무거운 사람을 right로 둔다.
3. 가장 가벼운 사람과 가장 무거운 사람이 함께 탈 수 있는지 확인한다.
4. 함께 탈 수 있으면 left를 오른쪽으로 이동한다.
5. 가장 무거운 사람은 항상 이번 보트에 타므로 right를 왼쪽으로 이동한다.
6. 보트를 한 대 사용했으므로 answer를 1 증가시킨다.
7. 모든 사람이 구출될 때까지 반복한다.

시간복잡도:
- O(N log N): 사람들의 몸무게를 정렬하기 때문이다.

공간복잡도:
- O(1): 별도의 추가 리스트 없이 포인터 변수만 사용한다.

배운 점:
- 정렬 후 양쪽 끝을 비교하면 가장 가벼운 사람과 가장 무거운 사람을 효율적으로 확인할 수 있다.
- 가장 무거운 사람은 매번 반드시 보트에 타므로 right는 항상 이동한다.
- 가장 가벼운 사람과 함께 탈 수 있을 때만 left를 이동한다.
- 탐욕법은 현재 상황에서 가장 합리적인 선택을 반복해 최적의 결과를 구하는 방식이다.
"""

def solution(people, limit):
    people.sort()

    left = 0
    right = len(people) - 1
    answer = 0

    while left <= right:
        if people[left] + people[right] <= limit:
            left += 1

        right -= 1
        answer += 1

    return answer
