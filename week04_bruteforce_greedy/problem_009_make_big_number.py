"""
문제명: 큰 수 만들기
출처: Programmers
유형: Greedy / Stack
난이도: Level 2

상태: Solved
날짜: 2026-07-22

문제 요약:
- 문자열 형태로 주어진 숫자 number에서 숫자 k개를 제거한다.
- 남은 숫자들의 순서는 원래 순서를 그대로 유지해야 한다.
- 만들 수 있는 가장 큰 숫자를 문자열로 반환한다.

풀이 접근:
1. 선택한 숫자를 저장할 빈 스택을 만든다.
2. number의 숫자를 왼쪽부터 하나씩 확인한다.
3. 아직 제거 횟수가 남아 있고, 스택의 마지막 숫자가 현재 숫자보다 작다면 제거한다.
4. 숫자를 하나 제거할 때마다 k를 1 감소시킨다.
5. 더 이상 제거할 수 없으면 현재 숫자를 스택에 추가한다.
6. 모든 숫자를 확인한 뒤에도 k가 남아 있다면 스택의 뒤에서 k개를 제거한다.
7. 스택의 숫자들을 문자열로 연결해 반환한다.

핵심 개념:
- 숫자의 순서는 바꿀 수 없고, 일부 숫자만 제거할 수 있다.
- 앞자리 숫자가 클수록 전체 숫자가 커진다.
- 현재 숫자보다 작은 앞 숫자를 즉시 제거하는 탐욕법을 사용한다.
- 리스트를 스택처럼 사용하고 pop()으로 마지막 숫자를 제거한다.

시간 복잡도:
- 각 숫자는 스택에 한 번 들어가고 최대 한 번 제거된다.
- 전체 시간 복잡도는 O(N)이다.
"""


def solution(number, k):
    # 최종적으로 남길 숫자들을 저장할 스택
    stack = []

    # number의 숫자를 왼쪽부터 하나씩 확인
    for digit in number:

        # 아직 제거 횟수가 남아 있고,
        # 스택이 비어 있지 않으며,
        # 스택의 마지막 숫자가 현재 숫자보다 작다면 반복
        while k > 0 and stack and stack[-1] < digit:

            # 앞에 있는 작은 숫자를 제거
            stack.pop()

            # 숫자를 하나 제거했으므로 k를 1 감소
            k -= 1

        # 현재 숫자를 스택에 추가
        stack.append(digit)

    # 모든 숫자를 확인한 뒤에도 k가 남아 있다면
    if k > 0:

        # 스택의 뒤에서 k개의 숫자를 제거
        stack = stack[:-k]

    # 리스트에 저장된 숫자를 하나의 문자열로 연결해 반환
    return "".join(stack)
